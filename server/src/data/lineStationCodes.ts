/** 모든 역 평탄화 — 검색 효율용 */
export const ALL_LINE_STATIONS: ReadonlyArray<LineStation> =
  (Object.keys(LINES_STATIONS) as LineKey[]).flatMap((k) => LINES_STATIONS[k]);

/**
 * 역명 → LineStation 매칭 (모든 호선 검색).
 * appName, kricName, simpleName, 괄호 안 별칭 모두 시도.
 * 같은 이름이 여러 호선에 있으면(환승역) 첫 매칭 반환 — lineCode 인자로 명시 가능.
 */
export function findStationByName(name: string, lineCode?: LineKey): LineStation | undefined {
  if (!name) return undefined;
  const norm = name.trim();
  const candidates = lineCode ? LINES_STATIONS[lineCode] ?? [] : ALL_LINE_STATIONS;
  // 1. simpleName(단순) 정확 일치
  let hit = candidates.find((s) => s.simpleName === norm);
  if (hit) return hit;
  // 2. stinNm(정식) 정확 일치
  hit = candidates.find((s) => s.stinNm === norm);
  if (hit) return hit;
  // 3. 괄호 안 별칭 (예: '당고개' → '불암산(당고개)')
  hit = candidates.find((s) => s.stinNm.includes(`(${norm})`));
  if (hit) return hit;
  // 4. 시작 일치 (마지막 보루)
  return candidates.find((s) => s.stinNm.startsWith(norm));
}
